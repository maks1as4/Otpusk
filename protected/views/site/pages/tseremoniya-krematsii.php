	<p>&nbsp;</p>
	<p style="text-align:center;">Вскоре оказалось, что человека не так то и просто сжечь. Поэтому принесли 4 заранее приготовленных газовых огнемета.</p>
	<p style="text-align:center;"><img src="/tmp/3_13.jpg" /></p>
	<p>&nbsp;</p>
	<p style="text-align:center;">Учтите, что без таких горелок кости вам сжечь не получится.</p>
	<p style="text-align:center;"><img src="/tmp/3_14.jpg" /></p>
	<p>&nbsp;</p>
	<p style="text-align:center;">Дальше было долгое и монотонное сжигание несжигаемого.</p>
