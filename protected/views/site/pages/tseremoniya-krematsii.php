	<p style="text-align:center;"><img src="/tmp/3_1.jpg" /></p>
	<p style="text-align:center;">На острове Бали принято сжигать трупы умерших. Церемония кремации - дорогостоящее мероприятие и если нет денег, то тело временно закапывают в землю, а семья умершего усиленно копит деньги несколько лет, чтобы сжечь умершего по-человечески.</p>
	<p style="text-align:center;">В этом году сжигали какого-то титулованного мертвеца. Фотографии под катом.</p>
	<p>&nbsp;</p>
	<p style="text-align:center;">С самого утра центральные улицы города переполнены.</p>
