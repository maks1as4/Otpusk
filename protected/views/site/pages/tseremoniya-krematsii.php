	<p>&nbsp;</p>
	<p style="text-align:center;">Участники торжественного шествия.</p>
	<p style="text-align:center;"><img src="/tmp/3_4.jpg" /></p>
	<p>&nbsp;</p>
	<p style="text-align:center;">Несут подношения и родственников умершего.</p>
