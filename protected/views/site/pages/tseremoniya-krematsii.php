	<p>&nbsp;</p>
	<p style="text-align:center;">Процедура подготовки к сжиганию довольно долгая. За это время некоторые успевают сделать зарисовки.</p>
	<p style="text-align:center;"><img src="/tmp/3_8.jpg" /></p>
